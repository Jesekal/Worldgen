package com.jeskal.worldgen.world;



public class ChunkRange {

    public final int minX, maxX;
    public final int minY, maxY;

    public ChunkRange(int minX, int maxX, int minY, int maxY) {
        this.minX = minX;
        this.maxX = maxX;
        this.minY = minY;
        this.maxY = maxY;
    }


}

